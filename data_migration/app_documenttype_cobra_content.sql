What is COBRA continuation coverage?

COBRA continuation coverage is a continuation of Plan coverage when it would otherwise 
end because of a life event. This is also called a “qualifying event.” Specific qualifying events 
are listed later in this notice. After a qualifying event, COBRA continuation coverage must be 
offered to each person who is a “qualified beneficiary.” You, your spouse, and your dependent 
children could become qualified beneficiaries if coverage under the Plan is lost because of 
the qualifying event. Under the Plan, qualified beneficiaries who elect COBRA continuation 
coverage must pay for COBRA continuation coverage. 

